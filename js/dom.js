class Element {
    //-------------------------------------------------------------------------------//
    // 							Element class constructor                    
    //-------------------------------------------------------------------------------//
    constructor(element, parent, label = '') {
        this.element = element;
        this.parent = parent;
        this.object = null;
        this.eventList = null;

        this.object = document.createElement(this.element);
        this.parent.appendChild(this.object);

        this.object.innerHTML = label;
    }
    //-------------------------------------------------------------------------------//
    // 								Custom attribute                     
    //-------------------------------------------------------------------------------//
    customAttribute(attr, val) {
        this.object.setAttribute(attr, val);
    }
    //-------------------------------------------------------------------------------//
    // 								Set attributes                     
    //-------------------------------------------------------------------------------//
    setAttributes(attrObj) {
        Object.assign(this.object, attrObj);
    }
    //-------------------------------------------------------------------------------//
    // 								Delete element                    
    //-------------------------------------------------------------------------------//
    delete() {
        this.object.parentNode.removeChild(this.object);
    }
    //-------------------------------------------------------------------------------//
    // 								Set element label                    
    //-------------------------------------------------------------------------------// 
    setLabel(label) {
        this.object.innerHTML = label;
    }
    //-------------------------------------------------------------------------------//
    // 			                    Event list					                    
    //-------------------------------------------------------------------------------// 
    eventList(events) {
        for (let i = 0; i < events.length; i++) {
            this.object.addEventListener(events[i].event, events[i].callback);
        }
    }
    //-------------------------------------------------------------------------------//
    // 								All events list                    
    //-------------------------------------------------------------------------------// 
    events(eventObj) {
        Object.assign(this.object, eventObj);
        this.eventList = eventObj;
    }
    //-------------------------------------------------------------------------------//
    // 								Insert Html                    
    //-------------------------------------------------------------------------------//
    html(html) {
        this.object.innerHTML = html;
    }
    //-------------------------------------------------------------------------------//
    // 								Clone events                    
    //-------------------------------------------------------------------------------//
    cloneEvents() {
        return Object.keys(this.eventList);
    }
    //-------------------------------------------------------------------------------//
    // 								Clone element                    
    //-------------------------------------------------------------------------------//
    cloneElement(element) {

    }
}